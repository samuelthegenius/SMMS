-- 2. Create function to sanitize user inputs
CREATE OR REPLACE FUNCTION sanitize_input(input_text text)
RETURNS text AS $$
BEGIN
    -- Remove potential SQL injection patterns
    RETURN regexp_replace(
        regexp_replace(
            regexp_replace(input_text, '[;''"]', '', 'g'),
            '(DROP|DELETE|INSERT|UPDATE|CREATE|ALTER|EXEC|UNION|SELECT)', '', 'gi'
        ),
        '(OR|AND)\s+\d+\s*=\s*\d+', '', 'gi'
    );
END;
$$ LANGUAGE plpgsql IMMUTABLE;

-- 3. Enhanced rate limiting with IP tracking
CREATE OR REPLACE FUNCTION check_rate_limit_enhanced(
    user_ip inet,
    action_type text,
    max_attempts integer,
    window_seconds integer
)
RETURNS boolean AS $$
DECLARE
    current_time timestamp with time zone := now();
    window_start timestamp with time zone := current_time - (window_seconds || ' seconds')::interval;
    attempt_count integer;
BEGIN
    -- Count attempts from this IP in the time window
    SELECT COUNT(*) INTO attempt_count
    FROM rate_limits_enhanced
    WHERE user_ip = user_ip
    AND action_type = action_type
    AND created_at >= window_start;
    
    -- Check if limit exceeded
    IF attempt_count >= max_attempts THEN
        RETURN false;
    END IF;
    
    -- Record this attempt
    INSERT INTO rate_limits_enhanced (user_ip, action_type, created_at)
    VALUES (user_ip, action_type, current_time);
    
    RETURN true;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- 4. Create enhanced rate limits table
CREATE TABLE IF NOT EXISTS rate_limits_enhanced (
    id bigint generated by default as identity primary key,
    user_ip inet not null,
    action_type text not null,
    created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- Enable RLS on enhanced rate limits
ALTER TABLE rate_limits_enhanced ENABLE ROW LEVEL SECURITY;

-- Policy: Only functions can access rate limits
CREATE POLICY "Enhanced rate limits are only accessible by functions" ON rate_limits_enhanced
    FOR ALL USING (false);

-- 5. Create audit trigger for sensitive operations
CREATE OR REPLACE FUNCTION enhanced_audit_trigger()
RETURNS trigger AS $$
BEGIN
    -- Log the IP address if available (from request context)
    -- Note: This requires the client IP to be passed in the request context
    IF TG_OP = 'INSERT' THEN
        INSERT INTO audit_logs (user_id, action, table_name, record_id, new_values)
        VALUES (
            auth.uid(), 
            'INSERT', 
            TG_TABLE_NAME, 
            NEW.id, 
            jsonb_build_object(
                'data', row_to_json(NEW),
                'sanitized', true
            )
        );
        RETURN NEW;
    ELSIF TG_OP = 'UPDATE' THEN
        INSERT INTO audit_logs (user_id, action, table_name, record_id, old_values, new_values)
        VALUES (
            auth.uid(), 
            'UPDATE', 
            TG_TABLE_NAME, 
            NEW.id, 
            row_to_json(OLD), 
            jsonb_build_object(
                'data', row_to_json(NEW),
                'sanitized', true
            )
        );
        RETURN NEW;
    ELSIF TG_OP = 'DELETE' THEN
        INSERT INTO audit_logs (user_id, action, table_name, record_id, old_values)
        VALUES (
            auth.uid(), 
            'DELETE', 
            TG_TABLE_NAME, 
            OLD.id, 
            row_to_json(OLD)
        );
        RETURN OLD;
    END IF;
    RETURN NULL;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- 6. Replace existing triggers with enhanced versions
DROP TRIGGER IF EXISTS audit_tickets ON tickets;
DROP TRIGGER IF EXISTS audit_profiles ON profiles;

CREATE TRIGGER enhanced_audit_tickets
    AFTER INSERT OR UPDATE OR DELETE ON tickets
    FOR EACH ROW EXECUTE PROCEDURE enhanced_audit_trigger();

CREATE TRIGGER enhanced_audit_profiles
    AFTER INSERT OR UPDATE OR DELETE ON profiles
    FOR EACH ROW EXECUTE PROCEDURE enhanced_audit_trigger();

-- 7. Create function to check for suspicious activity patterns
CREATE OR REPLACE FUNCTION detect_suspicious_activity(
    user_id uuid,
    time_window_minutes integer DEFAULT 60
)
RETURNS TABLE (
    activity_type text,
    count bigint,
    is_suspicious boolean
) AS $$
BEGIN
    RETURN QUERY
    SELECT 
        'ticket_creations' as activity_type,
        COUNT(*) as count,
        COUNT(*) > 10 as is_suspicious
    FROM audit_logs 
    WHERE user_id = detect_suspicious_activity.user_id 
    AND action = 'INSERT' 
    AND table_name = 'tickets'
    AND created_at >= now() - (time_window_minutes || ' minutes')::interval
    
    UNION ALL
    
    SELECT 
        'failed_logins' as activity_type,
        COUNT(*) as count,
        COUNT(*) > 5 as is_suspicious
    FROM audit_logs 
    WHERE user_id = detect_suspicious_activity.user_id 
    AND action LIKE '%failed%'
    AND created_at >= now() - (time_window_minutes || ' minutes')::interval;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- 8. Create security monitoring view
CREATE OR REPLACE VIEW security_dashboard AS
SELECT 
    p.full_name,
    p.email,
    p.role,
    COUNT(CASE WHEN al.action = 'INSERT' AND al.table_name = 'tickets' THEN 1 END) as tickets_created,
    COUNT(CASE WHEN al.action = 'UPDATE' AND al.table_name = 'tickets' THEN 1 END) as tickets_updated,
    MAX(al.created_at) as last_activity,
    CASE 
        WHEN COUNT(CASE WHEN al.action = 'INSERT' AND al.table_name = 'tickets' THEN 1 END) > 20 THEN 'High Activity'
        WHEN COUNT(CASE WHEN al.action = 'INSERT' AND al.table_name = 'tickets' THEN 1 END) > 10 THEN 'Medium Activity'
        ELSE 'Normal Activity'
    END as activity_level
FROM profiles p
LEFT JOIN audit_logs al ON p.id = al.user_id
WHERE al.created_at >= now() - interval '24 hours'
GROUP BY p.id, p.full_name, p.email, p.role
ORDER BY tickets_created DESC;

