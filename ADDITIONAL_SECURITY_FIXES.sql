-- 10. Create function to automatically clean up old audit logs
CREATE OR REPLACE FUNCTION cleanup_old_audit_logs()
RETURNS void AS $$
BEGIN
    -- Delete audit logs older than 90 days (adjust as needed for compliance)
    DELETE FROM audit_logs 
    WHERE created_at < now() - interval '90 days';
    
    -- Delete old rate limit entries older than 24 hours
    DELETE FROM rate_limits_enhanced 
    WHERE created_at < now() - interval '24 hours';
    
    DELETE FROM rate_limits 
    WHERE created_at < now() - interval '24 hours';
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

