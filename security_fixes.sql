-- 1. Drop existing rate limiting function to avoid naming conflicts
DROP FUNCTION IF EXISTS check_rate_limit(text,text,integer,integer);

-- 2. Create missing rate limiting function
CREATE OR REPLACE FUNCTION check_rate_limit(
    key_prefix text,
    action_type text,
    max_attempts integer,
    window_seconds integer
)
RETURNS boolean AS $$
DECLARE
    current_time timestamp with time zone := now();
    window_start timestamp with time zone := current_time - (window_seconds || ' seconds')::interval;
    attempt_count integer;
BEGIN
    -- Count attempts in the time window
    SELECT COUNT(*) INTO attempt_count
    FROM rate_limits
    WHERE key_prefix || '_' || action_type = key_prefix || '_' || action_type
    AND created_at >= window_start;
    
    -- Check if limit exceeded
    IF attempt_count >= max_attempts THEN
        RETURN false;
    END IF;
    
    -- Record this attempt
    INSERT INTO rate_limits (key_prefix, action_type, created_at)
    VALUES (key_prefix, action_type, current_time);
    
    RETURN true;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- 2. Create rate_limits table if it doesn't exist
CREATE TABLE IF NOT EXISTS rate_limits (
    id bigint generated by default as identity primary key,
    key_prefix text not null,
    action_type text not null,
    created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- Enable RLS on rate_limits
ALTER TABLE rate_limits ENABLE ROW LEVEL SECURITY;

-- Create policy for rate_limits (only function can access)
CREATE POLICY "Rate limits are only accessible by functions" ON rate_limits
    FOR ALL USING (false);

