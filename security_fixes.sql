-- 9. Create audit log table for security events
CREATE TABLE IF NOT EXISTS audit_logs (
    id bigint generated by default as identity primary key,
    user_id uuid references profiles(id),
    action text not null,
    table_name text,
    record_id bigint,
    old_values jsonb,
    new_values jsonb,
