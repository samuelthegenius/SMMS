-- 5. Create get_stale_tickets function for escalation monitor
CREATE OR REPLACE FUNCTION get_stale_tickets()
RETURNS TABLE (
    id bigint,
    title text,
    specific_location text,
    priority ticket_priority,
    created_at timestamp with time zone
) AS $$
BEGIN
    RETURN QUERY
    SELECT 
        t.id,
        t.title,
        t.specific_location,
        t.priority,
        t.created_at
    FROM tickets t
    WHERE t.status IN ('Pending', 'Assigned', 'In Progress')
    AND t.created_at < now() - interval '4 hours'
    ORDER BY t.priority DESC, t.created_at ASC;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

